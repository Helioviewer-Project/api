tend   = '{$tend}'

EOD;
                }


                break;
            }
        }
        if ($count > 0) {
            $string .= <<<EOD

a_xrt = vso_search(tstart, tend, instrument='XRT')
b_xrt= vso_get( a_XRT )

EOD;
        }else{
	        $string = '';
        }

        return $string;
    }

