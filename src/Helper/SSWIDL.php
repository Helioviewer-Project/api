;
; Hinode XRT data - downloadable via the VSO
;

EOD;
        $count = 0;
        foreach ( $this->_imageLayers as $i=>$layer) {
            if ( $layer['uiLabels'][1]['name'] == 'XRT' ) {
                $count++;

                if ( is_null($this->_end) ) {
                    $tstart = $tend = str_replace('/','-',$layer['subDate']) .' '.$layer['subTime'];
                    $string .= <<<EOD

